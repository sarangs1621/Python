# Python Module example

def add(a, b):
   result = a + b
   return result

def subs(a, b):
   result = a - b
   return result

def multi(a, b):
   result = a * b
   return result

def div(a, b):
   result = a / b
   return result

def floor(a, b):
   result = a // b
   return result

def modulas(a, b):
   result = a % b
   return result

def expo(a, b):
   result = a ** b
   return result
